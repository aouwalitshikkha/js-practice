// Math is Object

console.log(Math.E);  // Return value of E
console.log(Math.PI);  // Return value of Pi

var b = 4.5054
var c = -630.645

console.log(Math.abs(c)) // Return Absolute Value 
console.log(Math.floor(b))  // return 4
console.log(Math.ceil(b))  // return 5
console.log(Math.round(b))  // Return 5 Round figure of Math Number

console.log(Math.max(400, 500, 600)) // return maximum number 
console.log(Math.min(b, c))

// Power or Exponential

console.log(Math.pow(2, 3))  // Return 8 which is 2^3
console.log(Math.sqrt(9)) // Return Square root

// Random Number Generator 

console.log(Math.random())
console.log(Math.random() * 50 + 1)
console.log(Math.round(Math.random() * 50 + 1))









